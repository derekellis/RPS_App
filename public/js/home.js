$(document).ready(function(){
  $('#signin').hide();
  $('#signup').hide();
  $("a.signin").click(function(e){
    $('#signup').hide('slow');
    $('#signin').show('slow');
    $('#flash').hide('slow');
    e.preventDEfault();
  });
  $("a.signup").click(function(e){
    $('#signin').hide('slow');
    $('#signup').show('slow');
    $('#flash').hide('slow');
    e.preventDEfault();
  });
  $("a#login").click(function(e){
    $('#signup').hide('slow');
    $('#signin').show('slow');
    $('#flash').hide('slow');
    e.preventDEfault();
  });
});